

package PertemuanSatu;

public class LuasLingkaran {

  
    public static void main(String[] args) {
        
        //Deklasari
        double pi, r, luas;
        
        //Deskripsi
        r = 27.84;
        pi = 3.14;
        
        //Proses
        luas = pi * r * r;
                
        //Output
        System.out.print("Luas lingkaran = ");
        System.out.println(luas);
    }
    
}
